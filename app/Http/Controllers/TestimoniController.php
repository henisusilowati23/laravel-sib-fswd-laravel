     */
    public function store(Request $request)
    {
        // dd($request->all());
        $validatedData = $request->validate([
            'name' => 'required|max:255|string',
            'job' => 'required|max:255|string',
            'review' => 'required',
            'value' => 'required',
        ]);

        Testimoni::create($validatedData);
        return redirect()
            ->route('review.index');
    }

